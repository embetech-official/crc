uint16_t CRC16_MODBUS_UpdateUsingFormula(void const *in, size_t size, uint16_t crc) {
  uint8_t const *p = in;
  while(size--) {
    crc ^= *p++;
    for(uint8_t i = 0; i < 8; i++) {
      if(crc & 0x0001) {
        crc = (crc >> 1) ^ 0xA001;
      } else {
        crc >>= 1;
      }
    }
  }
  return crc;
}

